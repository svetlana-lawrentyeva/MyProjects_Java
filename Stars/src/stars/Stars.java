package stars;

public class Stars
{
    private static int size=7;
    public static void main(String[] args)
    {
        square();
        System.out.println();
        triangle1();
        System.out.println();
        triangle2();
        System.out.println();
        triangle3();
        System.out.println();
        triangle4();
        System.out.println();
        numbers1();
        System.out.println();
        numbers2();
        System.out.println();
        numbers3();
        System.out.println();
        numbers4();
    }
    private static void square()
    {
        for(int i=0; i<size;++i)
        {
            for(int j=0; j<size; ++j)
            {
                if(i==0|i==size-1|j==0|j==size-1)
                {
                    System.out.print("*");
                }
                else
                {
                    System.out.print(" ");
                }
            }
            System.out.println("");
        }
    }
    private static void triangle1()
    {
        for(int i=0; i<size;++i)
        {
            for(int j=i; j<size; ++j)
            {
                if(i==0|j==size-1|j==i)
                {
                    System.out.print("*");
                }
                else
                {
                    System.out.print(" ");
                }
            }
            System.out.println("");
        }
    }
    private static void triangle2()
    {
        for(int i=0; i<size;++i)
        {
            for(int j=0; j<size; ++j)
            {
                if(i==0|j==size-1|j==i)
                {
                    System.out.print("*");
                }
                else
                {
                    System.out.print(" ");
                }
            }
            System.out.println("");
        }
    }
    private static void triangle3()
    {
        for(int i=0; i<size;++i)
        {
            for(int j=0; j<size; ++j)
            {
                if(i==size-1|j==i|j==0)
                {
                    System.out.print("*");
                }
                else
                {
                    System.out.print(" ");
                }
            }
            System.out.println("");
        }
    }
    private static void triangle4()
    {
        for(int i=0; i<size;++i)
        {
            for(int j=0; j<size; ++j)
            {
                if(i==size-1|j==size-1|j==size-i-1)
                {
                    System.out.print("*");
                }
                else
                {
                    System.out.print(" ");
                }
            }
            System.out.println("");
        }
    }
    private static void numbers1()
    {
        for(int i=1; i<=size;++i)
        {
            for(int j=1; j<=size; ++j)
            {
                System.out.print(i);
            }
            System.out.println("");
        }
    }
    private static void numbers2()
    {
        for(int i=1; i<=size;++i)
        {
            for(int j=1; j<=size; ++j)
            {
                System.out.print(j);
            }
            System.out.println("");
        }
    }
    private static void numbers3()
    {
        for(int i=size; i>=1;--i)
        {
            for(int j=1; j<=size; ++j)
            {
                System.out.print(i);
            }
            System.out.println("");
        }
    }
    private static void numbers4()
    {
        for(int i=1; i<=size;++i)
        {
            for(int j=size; j>=1; --j)
            {
                System.out.print(j);
            }
            System.out.println("");
        }
    }
}
