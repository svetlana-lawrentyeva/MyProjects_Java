public abstract class Person
{
    private int id;
    private String fName;
    private String lName;
    private int age;
    
    public Person(int _id, String _fName, String _lName, int _age)
    {
        id=_id;
        fName=_fName;
        lName=_lName;
        age=_age;
    }
    public int getId()
    {
        return id;
    }
    public String getFName()
    {
        return fName;
    }
    public String getLName()
    {
        return lName;
    }
    public int getAge()
    {
        return age;
    }
}
