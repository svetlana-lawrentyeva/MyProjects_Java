package pkg12cycles;

import java.util.Scanner;

public class Main
{
  public static void main(String[] args)
  {
    Scanner sc=new Scanner(System.in);
    System.out.print("Введите число: ");
    int n=sc.nextInt();
    int f=1;
    for(int i=1;i<=n;++i)
    {
      f*=i;
    }
    System.out.println("Факториал: "+f);
  }
}
