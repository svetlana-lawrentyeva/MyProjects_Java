package pkg5cycles;

import java.util.Scanner;

public class Main
{
    public static void main(String[] args)
    {
        Scanner sc=new Scanner(System.in);
        System.out.println("Введите размер доски: ");
        int size=sc.nextInt();
        for(int i=0; i<size;++i)
        {
            for(int j=0;j<size;++j)
            {
                if(i%2==0)
                {
                    if(j%2==0)
                    {
                    System.out.print("*");
                    }
                    else
                    {
                        System.out.print(" ");
                    }
                }
                else
                {
                    if(j%2==0)
                    {
                    System.out.print(" ");
                    }
                    else
                    {
                        System.out.print("*");
                    }
                }
            }
            System.out.println();
        }
    }
}
